def get_package_hashes(series: str) -> list[bytes]:
    """Generate deterministic package hashes for a given series.

    Returns a list of SHA-256 hash bytes for each package in the series.
    """
    data = SERIES_PACKAGES[series]
    hashes = []
    for name, version in data["packages"]:
        h = hashlib.sha256(f"{name}={version}".encode()).digest()
        hashes.append(h)
    return hashes


def get_package_ids(series: str, seed: int = 0) -> list[int]:
    """Generate deterministic package IDs for a given series.

    These simulate the server-assigned package IDs that map to hashes.
    The seed allows different clients to have slightly different ID ranges.
    """
    rng = random.Random(seed)
    base = rng.randint(1, 100000)
    count = len(SERIES_PACKAGES[series]["packages"])
    return list(range(base, base + count))


def get_available_upgrade_ids(
    installed_ids: list[int],
    count: int = 5,
    seed: int = 0,
) -> list[int]:
    """Pick a random subset of installed package IDs to report as upgradeable.

    Simulates a machine that has a few pending security/regular upgrades.
    """
    rng = random.Random(seed)
    count = min(count, len(installed_ids))
    return sorted(rng.sample(installed_ids, count))


def get_security_upgrade_ids(
    upgrade_ids: list[int],
    count: int = 2,
    seed: int = 0,
) -> list[int]:
    """Pick a subset of available upgrades that are security updates."""
    rng = random.Random(seed)
    count = min(count, len(upgrade_ids))
    return sorted(rng.sample(upgrade_ids, count))
